public class DivisionBy7 {
    public static void main(String[]args){
        int n=48;
        int quotient=n/7;
        int product=quotient*7;
        if(n==product){
            System.out.print("it is divisible");
        }
        else{
            System.out.print("it is not divisible");
        }
    }
    
}
